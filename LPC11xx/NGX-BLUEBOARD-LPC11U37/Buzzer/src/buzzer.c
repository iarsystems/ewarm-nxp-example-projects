#include "lpc11uxx.h"
#include "gpio.h"
#include "buzzer.h"

/**
********************************************************************************************
  Function Name : buzzer_init()
  Description   :
  Input         :
  Output        : Void
  Note          :
********************************************************************************************
*/
void buzzer_init()
{
int i=0;

  GPIOSetDir( 0 , 17 , 1 );
  while(1)
  {
    for(i=0;i<1000000;i++);
      GPIOSetBitValue( 0, 17 , 0 );
    for(i=0;i<1000000;i++);
      GPIOSetBitValue( 0, 17 , 1 );
  }
 }

